package carService;

public class Car {
    String mark;
    String model;
    int productionDate;
    String faultDescription;


    Car(String mark, String model, int productionDate, String faultDescription){
        this.mark = mark;
        this.model = model;
        this.productionDate = productionDate;
        this.faultDescription = faultDescription;

    }

}
